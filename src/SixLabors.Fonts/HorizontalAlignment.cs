namespace SixLabors.Fonts
{
    /// <summary>
    /// Horizontal alignment modes.
    /// </summary>
    public enum HorizontalAlignment
    {
        /// <summary>
        /// Aligns text from the left.
        /// </summary>
        Left = 0,

        /// <summary>
        /// Aligns text from the right.
        /// </summary>
        Right = 1,

        /// <summary>
        /// Aligns text from the center.
        /// </summary>
        Center = 2
    }
}